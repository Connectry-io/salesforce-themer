#!/usr/bin/env node
/**
 * Effects Drift Detector
 *
 * Asserts that migrated effects are implemented via core/effects/engine.js
 * (not handwritten in each surface). Exits non-zero when drift is found.
 *
 * Why this exists: the refactor moved backgroundPattern (and over time will
 * move all 9 effects) into a single primitives engine. Without mechanical
 * enforcement, future edits will re-introduce per-surface handwritten values
 * — which is exactly the drift we just paid to remove.
 *
 * Run: node scripts/check-effects-drift.js
 * Returns: exit 0 on clean, 1 on drift.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const REPO = path.resolve(__dirname, '..');

// Effects that have been migrated to core/effects/engine.js.
// As each effect is migrated, add it here; the detector's assertions become
// active for that effect across all surfaces.
const MIGRATED_EFFECTS = [
  'backgroundPattern',
  'hoverLift',
];

// Forbidden patterns per migrated effect. Each entry describes where
// handwritten effect-specific values should NOT appear anymore.
// Add new entries as migration expands.
const RULES = {
  backgroundPattern: [
    {
      file: 'effects/effects.js',
      forbid: /_buildBackgroundPatternCss/,
      reason: 'backgroundPattern CSS is now generated by core/effects/engine.js; remove the legacy helper',
    },
    {
      file: 'options/options.css',
      forbid: /\[data-fx-background-pattern="(dot-?grid|lineGrid|line-grid|hatch|noise|subway|crosshatch)"\]/i,
      reason: 'Per-style background-pattern rules must be generated by the engine, not hardcoded in options.css',
    },
    {
      file: 'options/options.css',
      forbid: /guide-effect-preview\[data-style="(dotGrid|lineGrid|hatch|noise|subway|crosshatch)"\]/,
      reason: 'Per-style Guide background-pattern rules must be generated by the engine, not hardcoded in options.css',
    },
  ],
  hoverLift: [
    {
      file: 'effects/effects.js',
      // The engine now owns the lift/shadow numbers. If effects.js
      // reintroduces a raw `translateY(-Npx)` with a hardcoded value in a
      // hover-lift rule, that's drift.
      forbid: /sf-themer-fx-hover[^{]*:hover[^{]*\{[^}]*translateY\(-\d/,
      reason: 'hoverLift transforms must come from engine.renderRules, not hardcoded px values',
    },
  ],
};

// ────────────────────────────────────────────────────────────────────────────

function readFile(relPath) {
  return fs.readFileSync(path.join(REPO, relPath), 'utf8');
}

function intensityLaddersAligned() {
  // Parse INTENSITY_LADDER from core engine and compare against the legacy
  // LADDER object that still exists in options.js as a fallback. They MUST
  // produce identical results so the feature flag toggle is safe.
  const engine = require(path.join(REPO, 'core/effects/engine.js'));
  const canonical = engine.INTENSITY_LADDER;

  const optionsJs = readFile('options/options.js');
  // Look for the legacy fallback ladder object
  const match = optionsJs.match(/const LADDER = useEngine \? engine\.INTENSITY_LADDER : (\{[\s\S]*?\});/);
  if (!match) {
    return { ok: true, note: 'No legacy ladder found in options.js — migration complete' };
  }
  const legacyStr = match[1];
  // Evaluate the object literal safely (it's just numeric data)
  const legacy = eval('(' + legacyStr + ')');
  for (const level of ['subtle', 'medium', 'strong']) {
    if (legacy[level].mult !== canonical[level].mult ||
        legacy[level].speed !== canonical[level].speed) {
      return {
        ok: false,
        reason: `Legacy ladder in options.js diverges from engine canonical at ${level}: legacy=${JSON.stringify(legacy[level])} canonical=${JSON.stringify(canonical[level])}`,
      };
    }
  }
  return { ok: true, note: 'Legacy ladder matches engine canonical' };
}

// ────────────────────────────────────────────────────────────────────────────

function run() {
  const violations = [];

  // 1. Migrated-effect forbidden patterns
  for (const effect of MIGRATED_EFFECTS) {
    const rules = RULES[effect] || [];
    for (const rule of rules) {
      const content = readFile(rule.file);
      if (rule.forbid.test(content)) {
        violations.push({
          effect,
          file: rule.file,
          pattern: rule.forbid.toString(),
          reason: rule.reason,
        });
      }
    }
  }

  // 2. Intensity ladder alignment (legacy fallback vs canonical)
  const ladderCheck = intensityLaddersAligned();
  if (!ladderCheck.ok) {
    violations.push({ effect: '*', file: 'options/options.js', reason: ladderCheck.reason });
  }

  // Report
  if (violations.length === 0) {
    console.log('✓ effects drift check passed');
    console.log(`  migrated effects: ${MIGRATED_EFFECTS.join(', ')}`);
    if (ladderCheck.note) console.log(`  ${ladderCheck.note}`);
    return 0;
  }

  console.error('✗ effects drift detected:');
  for (const v of violations) {
    console.error(`  [${v.effect}] ${v.file}: ${v.reason}`);
    if (v.pattern) console.error(`     pattern: ${v.pattern}`);
  }
  console.error('');
  console.error('Fix: move effect logic into core/effects/engine.js and have');
  console.error('surface adapters consume it via renderRules() / primitives.');
  return 1;
}

process.exit(run());
