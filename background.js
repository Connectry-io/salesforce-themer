// ─── CSS generation + caching ─────────────────────────────────────────────────

async function cacheThemeCSS(themeId) {
  if (!themeId || themeId === 'none') return;

  // Check if it's a custom theme
  if (themeId.startsWith('custom-')) {
    return cacheCustomThemeCSS(themeId);
  }

  const theme = THEME_REGISTRY.themes.find(t => t.id === themeId);
  if (!theme) {
    console.warn('[Salesforce Themer] Unknown theme:', themeId);
    return;
  }
  try {
    const css = generateThemeCSS(theme);
    await chrome.storage.local.set({ [`themeCSS_${themeId}`]: css });
  } catch (err) {
    console.error('[Salesforce Themer] Failed to cache CSS for', themeId, err);
  }
}

async function cacheCustomThemeCSS(themeId) {
  try {
    const { customThemes } = await chrome.storage.sync.get('customThemes');
    if (!customThemes) return;

    const custom = customThemes.find(t => t.id === themeId);
    if (!custom) {
      console.warn('[Salesforce Themer] Unknown custom theme:', themeId);
      return;
    }

    // Find base theme
    const base = THEME_REGISTRY.themes.find(t => t.id === custom.basedOn);
    if (!base) {
      console.warn('[Salesforce Themer] Unknown base theme:', custom.basedOn);
      return;
    }

    // Resolve: base → core overrides → derivation → advanced overrides
    const resolvedColors = resolveCustomTheme(
      base.colors,
      custom.coreOverrides || {},
      custom.advancedOverrides || {}
    );

    const css = generateThemeCSS({ id: themeId, colors: resolvedColors });
    await chrome.storage.local.set({ [`themeCSS_${themeId}`]: css });
  } catch (err) {
    console.error('[Salesforce Themer] Failed to cache custom theme CSS:', themeId, err);
  }
}

async function cacheAllThemes() {
  // Cache OOTB themes
  for (const theme of THEME_REGISTRY.themes) {
    await cacheThemeCSS(theme.id);
  }

  // Cache custom themes
  try {
    const { customThemes } = await chrome.storage.sync.get('customThemes');
    if (customThemes && customThemes.length > 0) {
      for (const custom of customThemes) {
        await cacheCustomThemeCSS(custom.id);
      }
    }
  } catch (err) {
    console.error('[Salesforce Themer] Failed to cache custom themes:', err);
  }
}

// ─── Install / update handler ─────────────────────────────────────────────────

chrome.runtime.onInstalled.addListener(async (details) => {
  await loadThemeRegistry();

  if (details.reason === 'install') {
    await chrome.storage.sync.set({
      theme: 'connectry',
      autoMode: false,
      lastLightTheme: 'connectry',
      lastDarkTheme: 'connectry-dark',
      orgThemes: {},
      themeScope: 'both',
    });
  }

  await cacheAllThemes();
});

// Re-generate CSS when browser starts
chrome.runtime.onStartup.addListener(async () => {
  await loadThemeRegistry();
  await cacheAllThemes();
});

// Also regenerate on any service worker activation (covers extension refresh during dev)
(async () => {
  try {
    await loadThemeRegistry();
    await cacheAllThemes();
  } catch (_) {}
})();

// ─── Storage change handler ───────────────────────────────────────────────────

chrome.storage.onChanged.addListener(async (changes, area) => {
  if (area !== 'sync') return;
  if (changes.theme?.newValue) {
    // Ensure changed theme is cached (usually already is, but handles edge cases)
    if (!THEME_REGISTRY.themes.length) await loadThemeRegistry();
    await cacheThemeCSS(changes.theme.newValue);
  }
});

// ─── Keyboard commands ────────────────────────────────────────────────────────

chrome.commands.onCommand.addListener(async (command) => {
  if (!THEME_REGISTRY.themes.length) await loadThemeRegistry();

  const allIds = getAllThemeIds();
  const lightIds = new Set(getLightThemeIds());
  const darkIds = new Set(getDarkThemeIds());

  if (command === 'toggle-theme') {
    const result = await chrome.storage.sync.get({ theme: 'connectry', autoMode: false });
    if (result.autoMode) return;
    const idx = allIds.indexOf(result.theme);
    const next = allIds[(idx + 1) % allIds.length];
    await applyAndSaveTheme(next, lightIds, darkIds);
  }

  if (command === 'toggle-dark-light') {
    const result = await chrome.storage.sync.get({
      theme: 'connectry',
      lastLightTheme: 'connectry',
      lastDarkTheme: 'connectry-dark',
      autoMode: false,
    });
    if (result.autoMode) return;

    const next = darkIds.has(result.theme)
      ? result.lastLightTheme
      : result.lastDarkTheme;
    await applyAndSaveTheme(next, lightIds, darkIds);
  }
});

async function applyAndSaveTheme(themeId, lightIds, darkIds) {
  const updates = { theme: themeId };
  if (lightIds.has(themeId)) updates.lastLightTheme = themeId;
  else if (darkIds.has(themeId)) updates.lastDarkTheme = themeId;
  await chrome.storage.sync.set(updates);
  await cacheThemeCSS(themeId);
  await broadcastThemeToActiveTabs(themeId);
}

async function broadcastThemeToActiveTabs(themeId) {
  try {
    const tabs = await chrome.tabs.query({ active: true, currentWindow: true });
    for (const tab of tabs) {
      if (!tab.id) continue;
      chrome.tabs.sendMessage(tab.id, { action: 'setTheme', theme: themeId }).catch(() => {});
    }
  } catch (_) {}
}
