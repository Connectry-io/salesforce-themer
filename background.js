/**
 * Salesforce Themer — Service Worker (background.js)
 *
 * Responsibilities:
 *  1. On install/update: generate CSS for all themes via the engine and
 *     pre-cache into chrome.storage.local for zero-flash loading.
 *  2. Handle keyboard commands (cycle theme, toggle dark/light).
 *  3. Broadcast theme changes to active Salesforce tabs.
 *
 * Architecture note: the engine runs here (in the service worker) so that
 * content.js stays simple — it only reads pre-cached CSS strings from local
 * storage and injects them. No module imports needed in content scripts.
 */

